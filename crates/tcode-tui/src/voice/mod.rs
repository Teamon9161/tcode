    /// The failure this feature actually hit, and the fix for it: under a
    /// pseudoconsole the release is fabricated in the same instant as the
    /// press, so key-up says nothing — but auto-repeat still arrives, and the
    /// gap after the last repeat is the finger coming off the key. The gesture
    /// stays hold-to-talk; only how its end is observed changes.
    #[test]
    fn a_fabricated_release_falls_back_to_reading_the_hold_from_key_repeat() {
        let (mut voice, sent) = voice();
        voice.turn_on().expect("the fake backend starts");
        voice.on_event(VoiceEvent::Ready);

        voice.on_press(false);
        let outcome = voice.on_release();

        assert!(matches!(outcome, VoiceOutcome::Notice(_)));
        assert_eq!(voice.end_detect, EndDetect::Repeat);
        assert!(
            matches!(voice.state, VoiceState::Recording { .. }),
            "the take survives — the microphone is still open"
        );
        assert!(
            voice.gesture_help().contains("hold"),
            "still hold-to-talk: {}",
            voice.gesture_help()
        );

        // The key is still down, so repeats keep arriving. Each one is
        // evidence, never a second gesture.
        for _ in 0..5 {
            voice.on_press(false);
            voice.on_release();
        }
        assert!(matches!(voice.state, VoiceState::Recording { .. }));
        assert_eq!(sent.lock().unwrap().as_slice(), &[VoiceCmd::Start]);

        // Finger up: the repeats stop, and the next tick past the gap ends it.
        voice.pretend_recording_started(Duration::from_secs(2));
        voice.press.last = Instant::now() - Duration::from_secs(1);
        voice.tick();

        assert_eq!(voice.state, VoiceState::Transcribing);
        assert_eq!(
            sent.lock().unwrap().as_slice(),
            &[VoiceCmd::Start, VoiceCmd::Stop]
        );
    }

    /// The gap that means "released" has to clear the machine's own repeat
    /// rate, or a slow repeat setting would chop every take into fragments.
    #[test]
    fn the_release_gap_adapts_to_the_observed_repeat_rate() {
        let mut press = PressTrack::new();
        assert_eq!(
            press.idle_limit(),
            REPEAT_START,
            "before any repeat, wait out the OS repeat delay"
        );

        // The first gap is the repeat *delay*, which must not be mistaken for
        // the rate.
        press.note_repeat();
        assert_eq!(press.idle_limit(), REPEAT_START);

        press.last = Instant::now() - Duration::from_millis(200);
        press.note_repeat();
        assert!(
            press.idle_limit() >= Duration::from_millis(600),
            "a 200ms repeat interval needs more than 200ms of silence to call it released"
        );
    }

    /// A real finger is slower than `SYNTHETIC_RELEASE`, so a genuine misfire
