        voice.on_release();
        assert_eq!(voice.state, VoiceState::Transcribing);

        let outcome = voice.on_event(VoiceEvent::Transcript("  改一下 editor  ".into()));
        assert_eq!(outcome, VoiceOutcome::Insert("改一下 editor".into()));
